package org.stepic.droid.model;

public class Meta {
    private int page;
    private boolean has_next;
    private boolean has_previous;

    public int getPage() {
        return page;
    }

    public boolean isHas_next() {
        return has_next;
    }

    public boolean isHas_previous() {
        return has_previous;
    }
}
